		<p style="text-align: justify;"><strong>UDRUŽENJE ZA PROMOCIJU HRIŠĆANSKIH VREDNOSTI “MANNA”</strong> je udruženje građana – hrišćanskih vernika koji pripadaju raznim denominacijama, osnovano dana 24.03.2006.godine a registrovano rešenjem Ministarstva za državnu upravu I lokalnu samoupravu Republike Srbije od 27.10.2006.godine.</p>
<p style="text-align: justify;">“Manna” je nezavisna, nedenominacijska I neprofitna Izdavačka kuća.</p>
<p style="text-align: justify;"><strong>Ciljevi zbog kojih je «Manna» osnovana su sledeći:</strong></p>
<ul style="list-style-type: circle; text-align: justify;">
<li>Promocija Hrišćanskih vrednosti</li>
<li>Pružanje pomoći hrišćanskim vernicima u njihovom duhovnom rastu i unapređenju njihovog</li>
<li>verskog života putem štampane reči, audio i video zapisa.</li>
<li>Pružanje pomoći hrišćanskim crkvama u oblasti misije, učeništva i drugog,</li>
<li>Pružanje pomoći pojedincima i grupama, u razvoju talenata – darova i službi, sa ciljem</li>
<li>proslavljanja Gospoda Isusa Hrista kroz pojedince i njegovu Crkvu.</li>
</ul>
<p style="text-align: justify;"><strong>Udruženje “Manna” će ostvarivati postavljene ciljeve delovanjem u sledećim oblastima:</strong></p>
<ul style="list-style-type: circle; text-align: justify;">
<li>publikacija i distribucija knjiga, Biblija, časopisa, brošura, traktata i drugog materijala verskog i prosvetnog karaktera – utemeljenog na Svetom Pismu (Bibliji)</li>
<li>publikacija I distribucija hrišćanske muzike na nosačima zvuka I slike</li>
<li>publikacija I distribucija snimaka propovedi, naučavanja I sličnog materijala na nosačima zvuka I slike</li>
<li>Publikacija i distribucija raznog materijala za decu I za rad sa decom</li>
</ul>
<p style="text-align: justify;">Verujemo da će naše služenje kroz “Mannu” doneti mnogo dobrog roda za Carstvo Božije u Srbiji I šire, te da će biti blagoslov kako vernicima tako I onima koji će tek dolazi Isusu u vremenu poslednje žetve koja nam predstoji.</p>
<p style="text-align: justify;">Ovim želimo pružiti ruku saradnje svim hrišćanima koji dele sa nama istu viziju, te koji smatraju da im na bilo koji način možemo pomoći ili da oni mogu pomoći nama u ispunjenju naše misije.</p>
<p style="text-align: justify;">Udruženje za promociju hrišćanskih vrednosti „Manna“ nije nova verska organizacija i nije konkurencija nikome – mi jednostavno želimo biti Kanal kroz koji će dobri Bog mnoge pojedince, grupe i Crkve blagosloviti, inspirisati, ohrabriti, ojačati, poučiti&#8230; a kada god u tome uspemo to će biti neizmerna radost i nagrada za sve nas.</p>
